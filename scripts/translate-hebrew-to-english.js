function translateText(text) {
  // First check if it's a complete manufacturer name
  if (manufacturerTranslations[text]) {
    return manufacturerTranslations[text];
  }
  
  // Then check for mixed patterns
  if (mixedTranslations[text]) {
    return mixedTranslations[text];
  }
  
  // Then check for model names
  if (modelTranslations[text]) {
    return modelTranslations[text];
  }
  
  // Handle mixed Hebrew-English patterns that weren't caught above
  if (text.match(/[א-ת]/) && text.match(/[a-zA-Z0-9]/)) {
    // Try to translate Hebrew parts while keeping English/numbers
    let translatedText = text;
    
    // Replace Hebrew manufacturer names within mixed text
    for (const [hebrew, english] of Object.entries(manufacturerTranslations)) {
      if (text.includes(hebrew)) {
        translatedText = translatedText.replace(hebrew, english);
      }
    }
    
    // Replace Hebrew model parts
    for (const [hebrew, english] of Object.entries(modelTranslations)) {
      if (text.includes(hebrew)) {
        translatedText = translatedText.replace(hebrew, english);
      }
    }
    
    // If any translation was applied, return the result
    if (translatedText !== text) {
      return translatedText;
    }
  }
  
  // If no translation found, return original text
  return text;
}

function processManufacturerData(data) {
  if (typeof data === 'string') {
    return translateText(data);
  }
  
  if (Array.isArray(data)) {
    return data.map(item => processManufacturerData(item));
  }
  
  if (typeof data === 'object' && data !== null) {
    const result = {};
    for (const [key, value] of Object.entries(data)) {
      if (key === 'title' && typeof value === 'string') {
        result[key] = translateText(value);
      } else {
        result[key] = processManufacturerData(value);
      }
    }
    return result;
  }
  
  return data;
}

async function translateManufacturerFile() {
  try {
    console.log('Reading manufacturers_hebrew.json...');
    const inputPath = path.join(__dirname, '..', 'data', 'manufacturers_hebrew.json');
    const outputPath = path.join(__dirname, '..', 'data', 'manufacturers_arabic.json');
    
    if (!fs.existsSync(inputPath)) {
      console.error('manufacturers_hebrew.json not found!');
      process.exit(1);
    }
    
    const jsonData = JSON.parse(fs.readFileSync(inputPath, 'utf8'));
    
    console.log('Translating Hebrew text to English...');
    const translatedData = processManufacturerData(jsonData);
    
    console.log('Writing translated data to manufacturers_arabic.json...');
    fs.writeFileSync(outputPath, JSON.stringify(translatedData, null, 2), 'utf8');
    
    console.log('Translation completed successfully!');
    console.log(`Output file: ${outputPath}`);
    
    // Log some statistics
    const originalText = JSON.stringify(jsonData);
    const translatedText = JSON.stringify(translatedData);
    
    let translationCount = 0;
    let mixedTranslationCount = 0;
    
    // Count manufacturer translations
    for (const hebrewTerm in manufacturerTranslations) {
      const regex = new RegExp(hebrewTerm, 'g');
      const matches = originalText.match(regex);
      if (matches) {
        translationCount += matches.length;
      }
    }
    
    // Count mixed pattern translations
    for (const hebrewTerm in mixedTranslations) {
      const regex = new RegExp(hebrewTerm.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
      const matches = originalText.match(regex);
      if (matches) {
        mixedTranslationCount += matches.length;
      }
    }
    
    console.log(`\nTranslation Statistics:`);
    console.log(`- Total manufacturer translations applied: ${translationCount}`);
    console.log(`- Total mixed pattern translations applied: ${mixedTranslationCount}`);
    console.log(`- Total translations: ${translationCount + mixedTranslationCount}`);
    console.log(`- File size: ${(fs.statSync(outputPath).size / 1024).toFixed(2)} KB`);
    
  } catch (error) {
    console.error('Error during translation:', error);
    process.exit(1);
  }
}

